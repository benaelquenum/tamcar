export function t(lang: Lang, key: string, vars?: Record<string, string | number>): string {
  const raw = dict[lang]?.[key] ?? dict[DEFAULT_LANG][key] ?? key;
  if (!vars) return raw;
  return raw.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? `{${k}}`));
}
